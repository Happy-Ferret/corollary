pub fn lexToken_q(modifyCache: bool) -> P<CToken> {
    /* do */ {
        let pos = getPos;

        let inp = getInput;

        match alexScan((pos, inp), 0) {
            AlexEOF => {
                /* do */ {
                    handleEofToken;
                    CTokEof
                }
            },
            AlexError(inp_q) => {
                lexicalError
            },
            AlexSkip((pos_q, inp_q), len) => {
                /* do */ {
                    setPos(pos_q);
                    setInput(inp_q);
                    lexToken_q(modifyCache)
                }
            },
            AlexToken((pos_q, inp_q), len, action) => {
                /* do */ {
                    setPos(pos_q);
                    setInput(inp_q);
                    let tok = action(pos, len, inp);

                    when(modifyCache)(setLastToken(tok));
                    tok
                }
            },
        }
    }
}

