pub fn alexMove(__0: Position, __1: Char) -> Position {
    match (__0, __1) {
        (pos, ' ') => {
            incPos(pos, 1)
        },
        (pos, '\n') => {
            retPos(pos)
        },
        (pos, '\r') => {
            incOffset(pos, 1)
        },
        (pos, _) => {
            incPos(pos, 1)
        },
    }
}

